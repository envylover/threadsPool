void threadPool::Thread::wake_up(void)
{
	_wakeup.notify_one();
}


//--------------------------------------------------------------------


//--------------------------------------------------------------------
void threadPool::Thread::killThread(void)
{
	isAlive = false;
	_wakeup.notify_one();
}


//--------------------------------------------------------------------


//--------------------------------------------------------------------

void threadPool::Thread::setTask(Task const & task)
{
	_task = task;
}


//--------------------------------------------------------------------


//--------------------------------------------------------------------
void threadPool::Thread::setFinishCallback(const Task& task)
{
	onFinish = task;
}


